import { Invoice } from 'src/modules/invoices/entities/invoice';
import { Column, Entity, OneToMany, PrimaryGeneratedColumn } from 'typeorm';

@Entity('users')
export class User {
  @PrimaryGeneratedColumn()
  id: number;

  @Column()
  name: string;

  @Column()
  email: string;

  @Column()
  password: string;

  @OneToMany(() => Invoice, (invoice) => invoice.user)
  invoices: Invoice[];
}
